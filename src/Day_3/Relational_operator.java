package Day_3;

public class Relational_operator {

	public static void main(String[] args) {
		int a=21;
		int b=25;
		System.out.println("a==b="+(a==b));
		System.out.println("a!=b="+(a!=b));
		System.out.println("a<b="+(a<b));
		System.out.println("a>b="+(a>b));
		System.out.println("b<==a="+(b<=a));
		System.out.println("b>==b="+(b>=a));
		

	}

}
