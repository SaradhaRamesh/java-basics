package loooping;

public class For_each_array {

	public static void main(String[] args) {
		int number[]= {12,20,30,40,50};
		for(int a:number) 
		{
			System.out.print(a+",");
			}
		
	System.out.println();
	String name[]={"sara","madhu","joys","deepi","varsha"};
	for(String b:name) 
	{
		System.out.print(b+",");
		}
	}


}
